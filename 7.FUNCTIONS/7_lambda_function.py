# Lambda function

# a function without name is called as anonymous functions or lamda functions.

# used for instant use(i.e for one time usage)

# syntax

   # lambda argument_list:expression

square= lambda n:n*n

print(square(5))
print(square(90))
print(square(-53))


sum= lambda a,b:a+b

print(sum(90,56))
print(sum(-89,55))

# no need of return statement, it returns internally